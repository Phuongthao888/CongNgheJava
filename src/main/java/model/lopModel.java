package model;

public class lopModel {
	public String Malop;
	public String Tenlop;
	
	public lopModel() {
		super();
	}

	public lopModel(String malop, String tenlop) {
		super();
		Malop = malop;
		Tenlop = tenlop;
	}

	public String getMalop() {
		return Malop;
	}

	public void setMalop(String malop) {
		Malop = malop;
	}

	public String getTenlop() {
		return Tenlop;
	}

	public void setTenlop(String tenlop) {
		Tenlop = tenlop;
	}
	
	
}
